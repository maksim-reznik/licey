    def export_final_file(self):  # –ú–µ—Ç–æ–¥ –¥–ª—è —ç–∫—Å–ø–æ—Ä—Ç–∞ —Ñ–∏–Ω–∞–ª—å–Ω–æ–≥–æ –∞—É–¥–∏–æ—Ñ–∞–π–ª–∞
        if hasattr(self, 'combined_audio'):  # –ü—Ä–æ–≤–µ—Ä–∫–∞, —Å—É—â–µ—Å—Ç–≤—É–µ—Ç –ª–∏ –∫–æ–º–±–∏–Ω–∏—Ä–æ–≤–∞–Ω–Ω—ã–π –∞—É–¥–∏–æ—Ñ–∞–π–ª
            # –û–ø—Ä–µ–¥–µ–ª—è–µ–º –¥–æ–º–∞—à–Ω–∏–π –∫–∞—Ç–∞–ª–æ–≥ –≤ –∑–∞–≤–∏—Å–∏–º–æ—Å—Ç–∏ –æ—Ç –æ–ø–µ—Ä–∞—Ü–∏–æ–Ω–Ω–æ–π —Å–∏—Å—Ç–µ–º—ã
            if os.name == 'nt':  # –¥–ª—è Windows
                default_path = os.path.expanduser('~\\')  # –ü—É—Ç—å –∫ –¥–æ–º–∞—à–Ω–µ–º—É –∫–∞—Ç–∞–ª–æ–≥—É
            else:  # –¥–ª—è Linux –∏ MacOS
                default_path = os.path.expanduser('~/')  # –ü—É—Ç—å –∫ –¥–æ–º–∞—à–Ω–µ–º—É –∫–∞—Ç–∞–ª–æ–≥—É

            # –û—Ç–∫—Ä—ã–≤–∞–µ–º –¥–∏–∞–ª–æ–≥ –≤—ã–±–æ—Ä–∞ –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏–∏
            export_path = QtWidgets.QFileDialog.getSaveFileName(
                self,
                '–°–æ—Ö—Ä–∞–Ω–∏—Ç—å —Ñ–∞–π–ª',
                default_path,  # –∏—Å–ø–æ–ª—å–∑—É–µ–º –æ–ø—Ä–µ–¥–µ–ª–µ–Ω–Ω—ã–π –≤—ã—à–µ –ø—É—Ç—å
                'Audio Files (*.mp3)'  # –§–æ—Ä–º–∞—Ç –¥–ª—è —Å–æ—Ö—Ä–∞–Ω–µ–Ω–∏—è
            )[0]

            if export_path:  # –ï—Å–ª–∏ –ø—É—Ç—å –±—ã–ª –≤—ã–±—Ä–∞–Ω
                try:
                    self.combined_audio += self.volume_slid  # –ü—Ä–∏–º–µ–Ω–µ–Ω–∏–µ –≥—Ä–æ–º–∫–æ—Å—Ç–∏
                    self.combined_audio.export(export_path, format="mp3")  # –≠–∫—Å–ø–æ—Ä—Ç –≤ —Ñ–æ—Ä–º–∞—Ç mp3
                    self.console_update(f"–§–∞–π–ª —É—Å–ø–µ—à–Ω–æ —ç–∫—Å–ø–æ—Ä—Ç–∏—Ä–æ–≤–∞–Ω –∫–∞–∫ {export_path}")  # –û–±–Ω–æ–≤–ª–µ–Ω–∏–µ –∫–æ–Ω—Å–æ–ª–∏ ```python
                except Exception as e:  # –û–±—Ä–∞–±–æ—Ç–∫–∞ –∏—Å–∫–ª—é—á–µ–Ω–∏–π
                    self.console_update(f"–û—à–∏–±–∫–∞ –ø—Ä–∏ —ç–∫—Å–ø–æ—Ä—Ç–µ —Ñ–∞–π–ª–∞: {str(e)}")  # –í—ã–≤–æ–¥ —Å–æ–æ–±—â–µ–Ω–∏—è –æ–± –æ—à–∏–±–∫–µ
            else:
                self.console_update("–≠–∫—Å–ø–æ—Ä—Ç –æ—Ç–º–µ–Ω—ë–Ω")  # –°–æ–æ–±—â–µ–Ω–∏–µ –æ–± –æ—Ç–º–µ–Ω–µ —ç–∫—Å–ø–æ—Ä—Ç–∞
        else:
            self.show_error_message("–û—à–∏–±–∫–∞ –ø—É—Å—Ç–æ–π –¥–æ—Ä–æ–∂–∫–∏")  # –ü–æ–∫–∞–∑ —Å–æ–æ–±—â–µ–Ω–∏—è –æ–± –æ—à–∏–±–∫–µ
            self.console_update("–ù–µ—Ç –∫–æ–º–±–∏–Ω–∏—Ä–æ–≤–∞–Ω–Ω–æ–π –∞—É–¥–∏–æ–¥–æ—Ä–æ–∂–∫–∏ –¥–ª—è —ç–∫—Å–ø–æ—Ä—Ç–∞.")  # –û–±–Ω–æ–≤–ª–µ–Ω–∏–µ –∫–æ–Ω—Å–æ–ª–∏

    def truncate_filename(self, filename, max_length):  # –ú–µ—Ç–æ–¥ –¥–ª—è –æ–±—Ä–µ–∑–∫–∏ –∏–º–µ–Ω–∏ —Ñ–∞–π–ª–∞
        # –†–∞–∑–¥–µ–ª—è–µ–º –∏–º—è —Ñ–∞–π–ª–∞ –∏ —Ä–∞—Å—à–∏—Ä–µ–Ω–∏–µ
        name, ext = os.path.splitext(filename)  # –ü–æ–ª—É—á–µ–Ω–∏–µ –∏–º–µ–Ω–∏ –∏ —Ä–∞—Å—à–∏—Ä–µ–Ω–∏—è —Ñ–∞–π–ª–∞

        if len(name) > max_length:  # –ü—Ä–æ–≤–µ—Ä–∫–∞ –¥–ª–∏–Ω—ã –∏–º–µ–Ω–∏ —Ñ–∞–π–ª–∞
            return f"{name[:max_length]}...{ext}"  # –í–æ–∑–≤—Ä–∞—â–∞–µ–º –æ–±—Ä–µ–∑–∞–Ω–Ω–æ–µ –∏–º—è
        return filename  # –í–æ–∑–≤—Ä–∞—â–∞–µ–º –æ—Ä–∏–≥–∏–Ω–∞–ª—å–Ω–æ–µ –∏–º—è

    def console_update(self, txt):  # –ú–µ—Ç–æ–¥ –¥–ª—è –æ–±–Ω–æ–≤–ª–µ–Ω–∏—è –∫–æ–Ω—Å–æ–ª–∏
        # –°–æ—Ö—Ä–∞–Ω—è–µ–º —Å–æ–æ–±—â–µ–Ω–∏–µ –≤ –∏—Å—Ç–æ—Ä–∏—é
        self.history.append(txt)  # –î–æ–±–∞–≤–ª–µ–Ω–∏–µ —Å–æ–æ–±—â–µ–Ω–∏—è –≤ –∏—Å—Ç–æ—Ä–∏—é

    def show_history(self):  # –ú–µ—Ç–æ–¥ –¥–ª—è –ø–æ–∫–∞–∑–∞ –∏—Å—Ç–æ—Ä–∏–∏ –¥–µ–π—Å—Ç–≤–∏–π
        history_window = HistoryWindow(self)  # –°–æ–∑–¥–∞–Ω–∏–µ –æ–∫–Ω–∞ –∏—Å—Ç–æ—Ä–∏–∏
        history_window.history_text.setText("\n".join(self.history))  # –£—Å—Ç–∞–Ω–æ–≤–∫–∞ —Ç–µ–∫—Å—Ç–∞ –∏—Å—Ç–æ—Ä–∏–∏
        history_window.exec_()  # –û—Ç–æ–±—Ä–∞–∂–µ–Ω–∏–µ –æ–∫–Ω–∞ –∏—Å—Ç–æ—Ä–∏–∏

    def on_item_clicked(self, item):  # –ú–µ—Ç–æ–¥ –¥–ª—è –æ–±—Ä–∞–±–æ—Ç–∫–∏ –Ω–∞–∂–∞—Ç–∏—è –Ω–∞ —ç–ª–µ–º–µ–Ω—Ç —Å–ø–∏—Å–∫–∞
        self.used_name = item.text()  # –ü–æ–ª—É—á–µ–Ω–∏–µ –∏–º–µ–Ω–∏ –≤—ã–±—Ä–∞–Ω–Ω–æ–≥–æ —ç–ª–µ–º–µ–Ω—Ç–∞
        self.used_ID = self.audio_id[self.used_name]  # –ü–æ–ª—É—á–µ–Ω–∏–µ ID –≤—ã–±—Ä–∞–Ω–Ω–æ–≥–æ —ç–ª–µ–º–µ–Ω—Ç–∞
        display_name = self.truncate_filename(self.used_name, 20)  # –û–±—Ä–µ–∑–∫–∞ –∏–º–µ–Ω–∏ –¥–ª—è –æ—Ç–æ–±—Ä–∞–∂–µ–Ω–∏—è
        self.file_inf.setText(f'ID: {self.used_ID}      Name: {display_name}')  # –û–±–Ω–æ–≤–ª–µ–Ω–∏–µ –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏–∏ –æ —Ñ–∞–π–ª–µ

